// SelectPreset will change to one of the 5 user presets.
func SelectPreset(channelNumber int8) CommandSet {
	if channelNumber > 5 {
		channelNumber = 0
	}
	return CommandSet{
		&THRCommand{Type: msgTypeTwo, Payload: []byte{0x0b, 0x00, 0x0e, 0x00, 0x00, 0x00, 0x04, 0x00, 0x00, 0x00, 0x00, singleByteInt(channelNumber), 0x00, 0x00, 0x00, 0x00, 0x00}},
	}
}
